import os

output_dir = "/content/member6_results"
os.makedirs(output_dir, exist_ok=True)

print("输出文件夹已创建：", output_dir)

import pandas as pd

metrics_df = pd.read_csv("/content/model_evaluation_metrics.csv")
pred_df = pd.read_csv("/content/test_set_predictions.csv")

print("=== model_evaluation_metrics.csv ===")
print(metrics_df.head())
print("\n列名：", metrics_df.columns.tolist())

print("\n=== test_set_predictions.csv ===")
print(pred_df.head())
print("\n列名：", pred_df.columns.tolist())
print("\nshape:", pred_df.shape)

import matplotlib.pyplot as plt
from sklearn.metrics import roc_curve, auc

y_true = pred_df["True_Label"]

lr_probs = pred_df["Logistic Regression_Fraud_Probability"]
xgb_probs = pred_df["XGBoost_Fraud_Probability"]

lr_fpr, lr_tpr, _ = roc_curve(y_true, lr_probs)
xgb_fpr, xgb_tpr, _ = roc_curve(y_true, xgb_probs)

lr_auc = auc(lr_fpr, lr_tpr)
xgb_auc = auc(xgb_fpr, xgb_tpr)

plt.figure(figsize=(8,6))
plt.plot(lr_fpr, lr_tpr, label=f"Logistic Regression (AUC = {lr_auc:.4f})", linewidth=2)
plt.plot(xgb_fpr, xgb_tpr, label=f"XGBoost (AUC = {xgb_auc:.4f})", linewidth=2)
plt.plot([0, 1], [0, 1], linestyle="--", linewidth=1)

plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curve Comparison")
plt.legend()
plt.grid(alpha=0.3)

roc_path = os.path.join(output_dir, "roc_curve_comparison.png")
plt.savefig(roc_path, dpi=300, bbox_inches="tight")
plt.show()

print("已保存：", roc_path)

from sklearn.metrics import precision_recall_curve, average_precision_score

lr_precision, lr_recall, _ = precision_recall_curve(y_true, lr_probs)
xgb_precision, xgb_recall, _ = precision_recall_curve(y_true, xgb_probs)

lr_ap = average_precision_score(y_true, lr_probs)
xgb_ap = average_precision_score(y_true, xgb_probs)

plt.figure(figsize=(8,6))
plt.plot(lr_recall, lr_precision, label=f"Logistic Regression (AUPRC = {lr_ap:.4f})", linewidth=2)
plt.plot(xgb_recall, xgb_precision, label=f"XGBoost (AUPRC = {xgb_ap:.4f})", linewidth=2)

plt.xlabel("Recall")
plt.ylabel("Precision")
plt.title("Precision-Recall Curve Comparison")
plt.legend()
plt.grid(alpha=0.3)

pr_path = os.path.join(output_dir, "pr_curve_comparison.png")
plt.savefig(pr_path, dpi=300, bbox_inches="tight")
plt.show()

print("已保存：", pr_path)

import numpy as np

plot_metrics = ["Precision", "Recall", "F1_Score", "AUC_ROC"]
plot_df = metrics_df.set_index("Model_Name")[plot_metrics]

x = np.arange(len(plot_metrics))
width = 0.35

fig, ax = plt.subplots(figsize=(9,6))
ax.bar(x - width/2, plot_df.loc["Logistic Regression"], width, label="Logistic Regression")
ax.bar(x + width/2, plot_df.loc["XGBoost"], width, label="XGBoost")

ax.set_xticks(x)
ax.set_xticklabels(["Precision", "Recall", "F1", "AUC-ROC"])
ax.set_ylim(0, 1.05)
ax.set_ylabel("Score")
ax.set_title("Model Performance Metrics Comparison")
ax.legend()
ax.grid(axis="y", alpha=0.3)

for i, v in enumerate(plot_df.loc["Logistic Regression"]):
    ax.text(i - width/2, v + 0.02, f"{v:.3f}", ha="center", fontsize=9)

for i, v in enumerate(plot_df.loc["XGBoost"]):
    ax.text(i + width/2, v + 0.02, f"{v:.3f}", ha="center", fontsize=9)

bar_path = os.path.join(output_dir, "metrics_bar_chart.png")
plt.savefig(bar_path, dpi=300, bbox_inches="tight")
plt.show()

print("已保存：", bar_path)

from sklearn.metrics import confusion_matrix, ConfusionMatrixDisplay

lr_pred = pred_df["Logistic Regression_Predicted_Label"]
xgb_pred = pred_df["XGBoost_Predicted_Label"]

lr_cm = confusion_matrix(y_true, lr_pred)
xgb_cm = confusion_matrix(y_true, xgb_pred)

fig, axes = plt.subplots(1, 2, figsize=(14,6))

disp1 = ConfusionMatrixDisplay(confusion_matrix=lr_cm, display_labels=["Normal", "Fraud"])
disp1.plot(ax=axes[0], values_format="d", colorbar=False)
axes[0].set_title("Logistic Regression Confusion Matrix")

disp2 = ConfusionMatrixDisplay(confusion_matrix=xgb_cm, display_labels=["Normal", "Fraud"])
disp2.plot(ax=axes[1], values_format="d", colorbar=False)
axes[1].set_title("XGBoost Confusion Matrix")

cm_path = os.path.join(output_dir, "confusion_matrix_comparison.png")
plt.savefig(cm_path, dpi=300, bbox_inches="tight")
plt.show()

print("已保存：", cm_path)

print("Logistic Regression confusion matrix:")
print(lr_cm)

print("\nXGBoost confusion matrix:")
print(xgb_cm)

